function getOpacityColor(name) {
    switch (name) {
        case "Gigskaparna": return "rgba(255, 199, 0, 0.5)";
        case "Festen AB": return "rgba(255, 122, 0, 0.5)";
        case "Trance AB": return "rgba(0, 255, 200, 0.5)";
        case "Nattmingel AB": return "rgba(255, 102, 196, 0.5)";
        case "Neverending AB": return "rgba(0, 240, 255, 0.5)";
        case "No Mind AB": return "rgba(166, 255, 0, 0.5)";
        case "Banzai AB": return "rgba(255, 0, 60, 0.5)";
        case "Xtas Produktioner": return "rgba(218, 0, 255, 0.5)";
        case "Finliret AB": return "rgba(255, 214, 232, 0.5)";
    }
}

function getFullColor(name) {
    switch (name) {
        case "Gigskaparna": return "rgb(255, 199, 0)";
        case "Festen AB": return "rgb(255, 122, 0)";
        case "Trance AB": return "rgb(0, 255, 200)";
        case "Nattmingel AB": return "rgb(255, 102, 196)";
        case "Neverending AB": return "rgb(0, 240, 255)";
        case "No Mind AB": return "rgb(166, 255, 0)";
        case "Banzai AB": return "rgb(255, 0, 60)";
        case "Xtas Produktioner": return "rgb(218, 0, 255)";
        case "Finliret AB": return "rgb(255, 214, 232)";
    }
}